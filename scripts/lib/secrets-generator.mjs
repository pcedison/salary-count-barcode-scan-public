import crypto from 'crypto';

const SESSION_SECRET_BYTES = 48;
const ENCRYPTION_KEY_BYTES = 48;
const ENCRYPTION_SALT_BYTES = 32;

function toBase64Url(bytes) {
  return Buffer.from(bytes).toString('base64url');
}

export function generateDeploymentSecrets(options = {}) {
  const randomBytes = options.randomBytes ?? crypto.randomBytes;

  return {
    SESSION_SECRET: toBase64Url(randomBytes(SESSION_SECRET_BYTES)),
    ENCRYPTION_KEY: toBase64Url(randomBytes(ENCRYPTION_KEY_BYTES)),
    ENCRYPTION_SALT: toBase64Url(randomBytes(ENCRYPTION_SALT_BYTES)),
    USE_AES_ENCRYPTION: 'true'
  };
}

export function renderSecretsEnv(secrets, options = {}) {
  const generatedAt = options.generatedAt ?? new Date().toISOString();

  return [
    '# Generated by npm run secrets:generate',
    `# Generated at: ${generatedAt}`,
    '# Store these values in your deployment secret manager. Do not commit them.',
    `SESSION_SECRET=${secrets.SESSION_SECRET}`,
    `ENCRYPTION_KEY=${secrets.ENCRYPTION_KEY}`,
    `ENCRYPTION_SALT=${secrets.ENCRYPTION_SALT}`,
    `USE_AES_ENCRYPTION=${secrets.USE_AES_ENCRYPTION}`
  ].join('\n');
}

export function renderSecretsJson(secrets) {
  return JSON.stringify(secrets, null, 2);
}
